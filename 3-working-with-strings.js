let str1 = "Hello";
let str2 = "World!";

//Using the + operator
console.log("********Using the + operator*******\n");
console.log(str1 + str2);
console.log(str1 + "Big + str2");

console.log("********Adding Spacing*******\n");
//Adding spacing
str1 = "Hello";

console.log(str1 + str2);
console.log(str1 + "Big " + str2);

console.log("********Be careful with numbers!*******\n");
//Be careful with numbers!
let num1 = 1;
let num2 = "1";

console.log(num1 + num2);
console.log(num1 + 1);